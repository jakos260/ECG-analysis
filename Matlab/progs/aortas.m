c100(i)=cmax(i)/(1+((100-p0(i))/p1(i))^2);
c0(i)=cmax(i)/(1+((p0(i))/p1(i))^2);

'   i          Cmax    Cstat100     C100       V0       Vmax/V0    r0    c0'
[i cmax(i) cstat100(i) c100(i) v0(i) maxstretch(i) r0(i)    c0(i)]
clf
plot(x,y)
as=axis;
as(4)=300;
