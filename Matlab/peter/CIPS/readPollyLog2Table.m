function TABLE = readPollyLog2Table(fn)

HEADV5000 = header5000();
HEADV5500 = header5500();
HEADV6948 = header6948();

if exist(fn,'file')
    TABLE  = readtable(fn,'Delimiter',',');
    if size(TABLE,2) == size(HEADV5000,2)
        TABLE.Properties.VariableNames = HEADV5000;
    elseif size(TABLE,2) == size(HEADV5500,2)
        TABLE.Properties.VariableNames = HEADV5500;
    elseif size(TABLE,2) == size(HEADV6948,2)
        TABLE.Properties.VariableNames = HEADV6948;
