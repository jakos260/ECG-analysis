    %         str = strsplit(cell2mat(TABLE{i,end}),' ');
    %         for j=1:length(str)
    %             A(i,j) = str2num(str{j});
    %         end
    %     end
    %     TABLE = TABLE(:,1:end-1);
    %     for j=1:size(A,2)
    %         eval(['A' num2str(j) '= A(:,j);']);
    %         eval(['TABLE = [TABLE table(A' num2str(j) ')];']);
    %     end
    % elseif size(TABLE,2) > size(HEAD,1)
    %     HEAD = [HEAD EXTRANEW'];
    % end
